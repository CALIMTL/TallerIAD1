//¿Qué es un commit y por qué es importante?"
//Un commit es una instantánea de los cambios realizados en el código fuente de un proyecto. Es importante porque permite llevar un registro de las modificaciones, facilita la colaboración entre desarrolladores y ayuda a revertir cambios si es necesario.

//"¿Cuál es la diferencia entre git add y git commit?"
//git add se utiliza para agregar cambios específicos al área de preparación (staging area) antes de confirmarlos, mientras que git commit crea un nuevo commit con los cambios que han sido agregados al área de preparación.

//"Explícame qué hace el comando git push."
//El comando git push se utiliza para subir los commits locales a un repositorio remoto, permitiendo que otros colaboradores accedan a los cambios realizados en el proyecto.